public class Ex4 {
    public static void main(String[] args) {
        //int형 변수 i를 선언하고 0 저장
        int i = 0;
        //100까지 출력하는 while반복문
        while (i < 100) {
            i++;
            System.out.println(i);
        }
        //100부터 거꾸로 출력
        int j = 100;
        while (j > 0) {
            System.out.println(j);
            j--;
        }
    }
}
