public class IfEx2 {
    public static void main(String[] args) {
        // 임의의 양의 정수가 짝수이면 “2의 배수입니다.”
        // , 아니면 “2의 배수가 아닙니다.”를 출력하는 프로그램을 작성하시오.




        // int num을 선언하고 임의의 숫자를 저장한다
        int num = 65;

        if (num % 2 == 0) { // 만약 num이 짝수라면
            // - 2의 배수입니다 출력
            System.out.println("2의 배수입니다.");
        } else {    // 아니라면
            // - 2의 배수가 아닙니다 출력
            System.out.println("2의 배수가 아닙니다.");
        }






    }
}
