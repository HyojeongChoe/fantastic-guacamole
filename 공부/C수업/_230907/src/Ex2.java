public class Ex2 {
    public static void main(String[] args) {
        //int형 변수 num1을 선언하고 (25*5)를 저장
        int num1 = (25 * 5);

        //int형 변수 num2를 선언하고 (36-4)-72를 저장
        int num2 = (36 - 4);

        //int형 변수 num3를 선언하고 72를 저장
        int num3 = 72;

        //int형 변수 num4를 선언하고 5를 저장
        int num4 = 5;
        //int형 변수 result를 선언하고 {(num1)+(num2)-num3)/num4를 저장
        int result = (num1 + num2 - num3) / num4 ;

        //출력
        System.out.println(result);
    }
}
